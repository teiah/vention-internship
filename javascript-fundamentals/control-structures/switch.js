// switch case
const day = 'Sunday'

switch (day) {
  case 'Monday':
    console.log('Water the plants today')
    break
  case 'Tuesday':
    console.log('Do the grocery shopping!')
    break
  case 'Wednesday':
    console.log('Vacuum the floors!')
    break
  case 'Thursday':
    console.log('Do the laundry!')
    break
  case 'Friday':
    console.log('Clean the windows!')
    break
  case 'Saturday':
  case 'Sunday':
    console.log('Just relax!')
    break
  default:
    console.log('Invalid day!')
}
